function getStaffPortalI18nBootstrapScript(enabledLocales) {
  const json = JSON.stringify(STAFF_PORTAL_STRINGS);
  // Per-tenant enabled locales — injected from the deployment (STAFF_PORTAL_LOCALES env),
  // NOT hardcoded, so one tenant's language set can never affect another's. See
  // verify-portal-locale-isolation.js. Falls back to es/en if not provided.
  const locales = (Array.isArray(enabledLocales) && enabledLocales.length) ? enabledLocales : ['es', 'en'];
  const localesJson = JSON.stringify(locales);
  const defaultLocale = locales.indexOf('es') !== -1 ? 'es' : locales[0];
  return `<script>
(function(){
  'use strict';
  var STAFF_I18N = ${json};
  var STAFF_ENABLED_LOCALES = ${localesJson};
  var STAFF_DEFAULT_LOCALE = ${JSON.stringify(defaultLocale)};
  var STAFF_LOCALE_KEY = 'wh_staff_portal_locale';
  var STAFF_THEME_KEY = 'wh_staff_portal_theme';
  window.getStaffLocale = function(){
    try {
      var s = localStorage.getItem(STAFF_LOCALE_KEY);
      if (STAFF_ENABLED_LOCALES.indexOf(s) !== -1) return s;
    } catch(_){}
    return STAFF_DEFAULT_LOCALE;
  };
  window.getStaffTheme = function(){
    try {
      var t = localStorage.getItem(STAFF_THEME_KEY);
      if (t === 'dark' || t === 'light') return t;
    } catch(_){}
    return 'light';
  };
  window.t = function(key, vars){
    var loc = window.getStaffLocale();
    var pack = STAFF_I18N[loc] || STAFF_I18N.en || {};
    var text = pack[key];
    if (text == null) text = (STAFF_I18N.en && STAFF_I18N.en[key]) || key;
    if (vars && typeof vars === 'object') {
      Object.keys(vars).forEach(function(k){
        text = String(text).split('{' + k + '}').join(String(vars[k]));
      });
    }
    return text;
  };
  window.applyStaffTheme = function(){
    var theme = window.getStaffTheme();
    var html = document.documentElement;
    if (html) html.setAttribute('data-theme', theme === 'dark' ? 'dark' : 'light');
    var btn = document.getElementById('staff-theme-toggle');
    if (!btn) return;
    var isDark = theme === 'dark';
    btn.setAttribute('aria-pressed', isDark ? 'true' : 'false');
    btn.setAttribute('aria-label', window.t(isDark ? 'app.theme.switchToLight' : 'app.theme.switchToDark'));
    btn.setAttribute('title', btn.getAttribute('aria-label'));
    btn.classList.toggle('is-dark', isDark);
  };
  window.setStaffTheme = function(theme){
    if (theme !== 'light' && theme !== 'dark') return;
    try { localStorage.setItem(STAFF_THEME_KEY, theme); } catch(_){}
    window.applyStaffTheme();
  };
  window.toggleStaffTheme = function(){
    window.setStaffTheme(window.getStaffTheme() === 'dark' ? 'light' : 'dark');
  };
  window.bindStaffThemeToggle = function(){
    var btn = document.getElementById('staff-theme-toggle');
    if (!btn || btn._staffThemeBound) return;
    btn._staffThemeBound = true;
    btn.addEventListener('click', function(){
      window.toggleStaffTheme();
    });
  };
  window.applyStaffPortalI18n = function(root){
    var scope = root || document;
    scope.querySelectorAll('[data-i18n]').forEach(function(el){
      var k = el.getAttribute('data-i18n');
      if (!k) return;
      el.textContent = window.t(k);
    });
    scope.querySelectorAll('[data-i18n-placeholder]').forEach(function(el){
      var k = el.getAttribute('data-i18n-placeholder');
      if (!k) return;
      el.setAttribute('placeholder', window.t(k));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach(function(el){
      var k = el.getAttribute('data-i18n-title');
      if (!k) return;
      el.setAttribute('title', window.t(k));
    });
    scope.querySelectorAll('[data-i18n-aria]').forEach(function(el){
      var k = el.getAttribute('data-i18n-aria');
      if (!k) return;
      el.setAttribute('aria-label', window.t(k));
    });
    scope.querySelectorAll('option[data-i18n]').forEach(function(el){
      var k = el.getAttribute('data-i18n');
      if (!k) return;
      el.textContent = window.t(k);
    });
    var html = document.documentElement;
    if (html) html.setAttribute('lang', window.getStaffLocale());
    document.querySelectorAll('.staff-lang-btn').forEach(function(btn){
      var lang = btn.getAttribute('data-lang');
      btn.classList.toggle('is-active', lang === window.getStaffLocale());
    });
    window.applyStaffTheme();
  };
  window.setStaffLocale = function(loc){
    if (STAFF_ENABLED_LOCALES.indexOf(loc) === -1) return;
    try { localStorage.setItem(STAFF_LOCALE_KEY, loc); } catch(_){}
    window.applyStaffPortalI18n(document);
    if (typeof window.staffPortalOnLocaleChange === 'function') {
      window.staffPortalOnLocaleChange(loc);
    }
  };
  window.bindStaffLangSwitch = function(){
    document.querySelectorAll('.staff-lang-btn').forEach(function(btn){
      if (btn._staffLangBound) return;
      btn._staffLangBound = true;
      btn.addEventListener('click', function(){
        var lang = btn.getAttribute('data-lang');
        if (lang) window.setStaffLocale(lang);
      });
    });
  };
  document.addEventListener('DOMContentLoaded', function(){
    window.applyStaffPortalI18n(document);
    window.bindStaffLangSwitch();
    window.bindStaffThemeToggle();
  });
})();
</script>`;
}
