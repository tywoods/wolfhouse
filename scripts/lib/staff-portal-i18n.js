function getStaffPortalI18nBootstrapScript() {
  const json = JSON.stringify(STAFF_PORTAL_STRINGS);
  return `<script>
(function(){
  'use strict';
  var STAFF_I18N = ${json};
  var STAFF_LOCALE_KEY = 'wh_staff_portal_locale';
  window.getStaffLocale = function(){
    try {
      var s = localStorage.getItem(STAFF_LOCALE_KEY);
      if (s === 'it' || s === 'en' || s === 'es') return s;
    } catch(_){}
    return 'en';
  };
  window.t = function(key, vars){
    var loc = window.getStaffLocale();
    var pack = STAFF_I18N[loc] || STAFF_I18N.en || {};
    var text = pack[key];
    if (text == null) text = (STAFF_I18N.en && STAFF_I18N.en[key]) || key;
    if (vars && typeof vars === 'object') {
      Object.keys(vars).forEach(function(k){
        text = String(text).split('{' + k + '}').join(String(vars[k]));
      });
    }
    return text;
  };
  window.applyStaffPortalI18n = function(root){
    var scope = root || document;
    scope.querySelectorAll('[data-i18n]').forEach(function(el){
      var k = el.getAttribute('data-i18n');
      if (!k) return;
      el.textContent = window.t(k);
    });
    scope.querySelectorAll('[data-i18n-placeholder]').forEach(function(el){
      var k = el.getAttribute('data-i18n-placeholder');
      if (!k) return;
      el.setAttribute('placeholder', window.t(k));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach(function(el){
      var k = el.getAttribute('data-i18n-title');
      if (!k) return;
      el.setAttribute('title', window.t(k));
    });
    scope.querySelectorAll('[data-i18n-aria]').forEach(function(el){
      var k = el.getAttribute('data-i18n-aria');
      if (!k) return;
      el.setAttribute('aria-label', window.t(k));
    });
    scope.querySelectorAll('option[data-i18n]').forEach(function(el){
      var k = el.getAttribute('data-i18n');
      if (!k) return;
      el.textContent = window.t(k);
    });
    var html = document.documentElement;
    if (html) html.setAttribute('lang', window.getStaffLocale());
    document.querySelectorAll('.staff-lang-btn').forEach(function(btn){
      var lang = btn.getAttribute('data-lang');
      btn.classList.toggle('is-active', lang === window.getStaffLocale());
    });
  };
  window.setStaffLocale = function(loc){
    if (loc !== 'en' && loc !== 'it' && loc !== 'es') return;
    try { localStorage.setItem(STAFF_LOCALE_KEY, loc); } catch(_){}
    window.applyStaffPortalI18n(document);
    if (typeof window.staffPortalOnLocaleChange === 'function') {
      window.staffPortalOnLocaleChange(loc);
    }
  };
  window.bindStaffLangSwitch = function(){
    document.querySelectorAll('.staff-lang-btn').forEach(function(btn){
      if (btn._staffLangBound) return;
      btn._staffLangBound = true;
      btn.addEventListener('click', function(){
        var lang = btn.getAttribute('data-lang');
        if (lang) window.setStaffLocale(lang);
      });
    });
  };
  document.addEventListener('DOMContentLoaded', function(){
    window.applyStaffPortalI18n(document);
    window.bindStaffLangSwitch();
  });
})();
</script>`;
}

